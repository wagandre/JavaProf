package Lessons.lesson10.task1;

public class PrimitiveComparing {
    public static void main(String[] args) {
        int a = 5;
        int b = 10;
        int c = 5;

        boolean result = a == b;
        System.out.println("Result a and b = " + result);
        result = a == c;
        System.out.println("Result a and c = " + result);


    }
}
